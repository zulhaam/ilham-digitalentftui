<h1>Macam-macam Format Audio</h1>
    <h3><strong>A. FORMAT-FORMAT AUDIO</strong></h3>
    <p><strong>1. AAC (Advanced Audio Coding) [ .m4a ]</strong></p>
    <p><strong>- </strong>AAC bersifat lossy  compression (data hasil kompresi tidak bisa dikembalikan lagi ke data sebelum  dikompres secara sempurna, karena setelah dikompres terdapat data-data yang  hilang). </p>
    <p>- AAC merupakan audio  codec yang menyempurnakan MP3 dalam hal medium dan high bit rates. </p>
    <p><strong>Cara kerja : </strong></p>
    <p>1.  Bagian-bagian sinyal yang tidak  relevan dibuang. </p>
    <p>2. Menghilangkan  bagian-bagian sinyal yang redundan. </p>
    <p>3. Dilakukan proses MDCT (Modified  Discret Cosine Transform) berdasarkan tingkat kekompleksitasan sinyal. </p>
    <p>4. <span dir="LTR"> </span>Adanya penambahan  Internal Error Correction. </p>
    <p>5. <span dir="LTR"> </span>Kemudian, sinyal disimpan atau  dipancarkan. </p>
    <p><strong>Kelebihan AAC dari MP3 : </strong></p>
    <p>- Sample ratenya antara  8 Hz – 96 kHz, sedangkan MP3 16 Hz – 48 kHz. </p>
    <p>- Memiliki 48 channel. </p>
    <p><span dir="LTR"> </span>- Suara lebih bagus  untuk kualitas bit yang rendah (dibawah 16 Hz). </p>
    <p><strong>Software  pendukung AAC :</strong> </p>
    <p>- IPod dan Itunes, Winamp.</p>
    <p><strong>Handphone : </strong>Nokia N91, Sony Ericsson  W800, dan Motorola ROKR E1. </p>
    <p> <strong>Hardware: </strong>Play Station Portable (PSP)  pada Agustus 2005. </p>
    <p><strong>2</strong>. <strong>WAVEFORM AUDIO [ .WAV  ]</strong></p>
    <p> - WAV adalah<strong> format audio standar  Microsoft dan IBM</strong> untuk PC. </p>
    <p>- WAV biasanya  menggunakan coding PCM (Pulse Code Modulation) </p>
    <p><span dir="LTR"> </span>- WAV adalah data tidak terkompres  sehingga seluruh sampel audio disimpan semuanya di harddisk. </p>
    <p>- <strong>Software</strong> yang dapat  menciptakan WAV dari Analog Sound misalnya adalah Windows Sound Recorder. </p>
    <p><span dir="LTR"> </span>- WAV jarang sekali digunakan di  internet karena ukurannya yang relatif besar. </p>
    <p><span dir="LTR"> </span>- Maksimal ukuran file WAV adalah 2GB. </p>
    <p><strong>3</strong>. <strong>Audio Interchange  File Format [.AIF]</strong></p>
    <p><strong>- </strong> Merupakan <strong>format standar Macintosh</strong>. </p>
    <p>- <strong>Software</strong> pendukung: Apple QuickTime </p>
    <p><strong>4. Audio CD [.cda]</strong></p>
    <p><strong>- </strong> <strong>Format</strong> untuk mendengarkan CD Audio </p>
    <p>- CD Audio stereo berkualitas sama  dengan PCM/WAV yang memiliki sampling rate 44100 Hz, 2 Channel (stereo) pada 16  bit. </p>
    <p>- Durasi = 75 menit dan dynamic range =  95 dB. </p>
    <p><strong>5. Mpeg Audio Layer 3  [.mp3]</strong></p>
    <p>- Merupakan file dengan lossy  compression. </p>
    <p>- Sering digunakan di internet karena  ukurannya yang cukup kecil dibandingkan ukuran audio file yang tidak  terkompresi.</p>
    <p>- Distandarisasi pada  tahun 1991. </p>
    <p>- Kompresi dilakukan dengan  menghilangkan bagian-bagian bunyi yang kurang berguna bagi pendengaran manusia.</p>
    <p>- Kompresi mp3 dengan kualitas 128 bits  44000 Hz biasanya akan menghasilkan file berukuran 3-4 MB, tetapi unsur panjang  pendeknya lagu juga akan berpengaruh. </p>
    <p>- <strong>Software </strong>pemutar file  mp3 : Winam, AIMP3</p>
    <p>- Software encoder : LAME (Lame ain&rsquo;t  MP3 Encoder), sebuah encoder mp3 open source dan freeware yang dibuat oleh Mike  Cheng pada awal tahun 1998. </p>
    <p>- Macam-macam bit rate:  32, 40, 48, 56, 64, 80, 96, 112, 128, 160, 192, 224, 256 and 320 kbit/s </p>
