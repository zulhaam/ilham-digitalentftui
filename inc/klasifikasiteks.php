 <h1><strong>Klasifikasi Teknik Kompresi Data</strong></h1>
    <h4><strong>1. <u>Entropy Encoding</u></strong>    </h4>
    <p>- Bersifat loseless</p>
    <p>- <span dir="LTR"> </span>Tekniknya tidak  berdasarkan media dengan spesifikasi dan karakteristik tertentu namun  berdasarkan urutan data</p>
    <p>- Statistical encoding, tidak  memperhatikan semantik data. </p>
    <p>- Mis: Run-length coding, Huffman  coding, Arithmetic coding </p>
    <h4>2. <u>Source Coding</u></h4>
    <p>- Bersifat Loosy</p>
    <p>- Berkaitan dengan data semantik (arti  data) dan media. </p>
    <p>- Mis: Prediction (DPCM, DM),  Transformation (FFT, DCT), Layered </p>
    <p>- Coding  (Bit position, subsampling, sub-band coding), Vector quantization </p>
    <h4><br>
      <u>3. Hybrid Coding</u></h4>
    <p>- Gabungan antara lossy + loseless, mis:  JPEG, MPEG, H.261 </p>