    <p><strong>2. PNG</strong></p>
    <p><center><img src="image/imgae.png" width="487" height="171"><br>
     </center></p>
    <p>Extensi : *.png<br>
      Warna Maximum : 8 bit (256 warna) dan 24 bit (16,7 juta warna)<br>
      Kegunaan : Web Graphics &amp; Gambar Screenshoot<br>
      Keunggulan : File berukuran kecil, transparasi+alpha dan web graphics<br>
      Kelemahan : faktor kompresi yang lebih baik dibandingkan dengan GIF (kurang lebih 5%-25% lebih baik dibanding format GIF, tapi tidak bisa animasi seperti gif<br>
      Kesimpulan : Format yang sangat baik untuk gambar web dan screenshot/gambar yg ingin diedit kembali.    </p>
    <p><strong>3. GIF</strong></p>
    <p><center><img src="image/image.gif" width="426" height="235"></center></p>
    <p>Extensi : *.gif<br>
      Warna Maximum : 8 bit (256 warna)<br>
      Kegunaan : Web graphics dan animasi<br>
      Keunggulan : File berukuran kecil, transparansi, animasi<br>
      Kelemahan : Tidak cocok untuk fotografis<br>
    Kesimpulan : Graphic Web dengan area warna flat/datar</p>
    <p><strong>4. TIFF</strong></p>
    <p><center><img src="image/tiff_screen_silhouettes_01.jpg" width="450" height="207"></center></p>
    <p><br>
      Extensi : *.tiff<br>
      Warna Maximum : 24 bit (16,7 juta warna)<br>
      Kegunaan : Desktop publishing/cetak dan fotografi<br>
      Keunggulan : Dapat dikompresi dengan kualitas gambar tetap<br>
      Kelemahan : Ukuran file besar, masalah kompabilitas<br>
    Kesimpulan : Format untuk gambar yang lebih mengutamakan kualitas, ukuran besar tidak masalah.</p>
    <p><strong>5. BMP</strong></p>
    <p><center><img src="image/image.bmp" width="512" height="149"></center></p>
    <p><br>
      Extensi : *.bmp<br>
      Warna Maximum : 24 bit (16,7 juta warna)<br>
      Kegunaan : Icon dan wallpaper windows<br>
      Keunggulan : Kualitas gambar tetap<br>
      Kelemahan : Ukuran filenya sangat besar<br>
    Kesimpulan : Format yang dipakai untuk wallpaper windows desktop</p>
    <p><strong>JPG, PNG atau GIF: Mana yang harus diplih?</strong><br>
    Kualitas foto dan gambar ilustrasi yang digunakan pada situs web,   percaya atau tidak dapat membuat banyak perbedaan. </p>
    <p>Anda dapat   menggunakan gambar yang paling baik atau yang biasa-biasa saja, atau   menggunakan tools capture profesional untuk mengeditnya, tetapi   kebutuhan dasarnya tetaplah sama . Untuk output gambar harus tajam dan   ukuran file yang dalam batas yang wajar.</p>
    <p> Sekarang, JPG, PNG atau GIF adalah tiga format foto/gambar paling   populer di web/blog. Tentu saja ada format lain disebut BMP tetapi   jarang sekali digunakan.</p>
    <p><strong>Teks &amp; Clipart</strong><br>
    Jika Anda ingin capturing text (seperti mem-blok kode surnber atau   halarnan pencarian di search engine Google atau menu navigasi), selalu   gunakan format GIF atau PNG â€“ karena screenshot akan lebih jelas dan   ukuran file yang tetap kecil.</p>
    <p><strong>Regular Desktop Windows</strong><br>
      Jika Anda ingin mengcapture/mengambil gambar jendela pada layar windows,   kotak dialog, Windows Explorer, DOS command prompt, Google Maps atau   bahkan layar splash gunakan format PNG.<br>
    </p>
    <p>Keuntungan menggunkan format PNG adalah menjaga semua kualitas warna dan   rnenghasilkan output yang lebih terang dibandingkan dengan format JPG.   Untuk test, gunakan Photoshop splash screen untuk perbandingan antara   kualitas JPEG dan PNG.</p>
    <p><strong>Foto &amp; Video</strong><br>
      Jika Anda mengcapture gambar screenshot dari video (YouTube video   Flash), Google Earth, video games, Flash anirnasi, desktop wallpaper   atau foto (seperti Flickr). Selalu pilih format JPG daripada PNG, karena   ukuran file gambar akan lebih kecil tanpa banyak degradasi dalam   kualitas.<br>
    </p>
    <p>Sebagai contoh, video di YouTube akan membutuhkan waktu sekitar 92kb   jika disimpan dalam format PNG, tetapi akan berubah menjadi 20kb jika   kita mengubahnya dari PNG menjadi format JPG. Yang mengherankan, tidak   ada perbedaan kualitas luar biasa yang terjadi setelah kita melakukan   perubah</p>