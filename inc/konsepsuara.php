    <h1>Konsep Dasar Audio (Suara)</h1>
    <p><strong>A. Suara </strong>adalah fenomena fisik yang dihasilkan oleh  getaran benda, getaran suatu benda  yang berupa sinyal analog dengan amplitudo yang berubah secara kontinyu  terhadap waktu</p>
    <p><center><img src="image/konsepsuara.png" width="553" height="70"></center></p>
    <p>- Suara berhubungan  erat dengan rasa &ldquo;mendengar&rdquo;.</p>
    <p>- Suara/bunyi  biasanya merambat melalui udara. Suara/bunyi tidak bisa merambat melalui ruang  hampa. </p>
    <p>- Suara  dihasilkan oleh getaran suatu benda. Selama bergetar, perbedaan tekanan terjadi  di udara sekitarnya. Pola osilasi yang terjadi dinamakan sebagai <strong>&ldquo;GELOMBANG&rdquo;</strong>. </p>
    <p>- Gelombang  mempunyai pola sama yang berulang pada interval tertentu, yang disebut sebagai <strong>&ldquo;PERIODE&rdquo;</strong>. <br>
      Contoh  suara periodik : instrument musik, nyanyian burung, dll Contoh suara  nonperiodik : batuk, percikan ombak, dll</p>
    <p><center><img width="449" height="289" src="image/suara1.jpg"></center></p>
    <p>Suara berkaitan erat  dengan: </p>
    <p><strong>1. Frekuensi </strong></p>
    <p><strong>- </strong>Banyaknya periode  dalam 1 detik</p>
    <ol>
    <blockquote>
      <p>- Satuan : <em>Hertz (Hz)</em> atau <em>cycles per second (cps)</em></p>
      <p><em>- </em>Panjang gelombang suara (wavelength)  dirumuskan = c/f Dimana c = kecepatan rambat bunyi. Dimana  f = frekuensi </p></blockquote>
    </ol>
    <p><u><strong>Contoh: </strong></u></p>
    <p>Berapakah panjang  gelombang untuk gelombang suara yang memiliki kecepatan rambat 343 m/s dan  frekuensi 20 kHz? </p>
    <p><u><strong>Jawab: </strong></u><br>
      WaveLength  = c/f = 343/20 = 17,15 mm. </p>
    <p>- Berdasarkan frekuensi, suara dibagi menjadi:
    <p>Infrasound                                   0Hz – 20 Hz <br>
    <p>Pendengaran Manusia                          20Hz – 20 KHz <br>
    <p>Ultrasound                                   20KHz – 1 GHz </p>
    <p>Hypersound                                   1GHz – 10 THz </p>
    <p>- Manusia  membuat suara dengan frekuensi : 50Hz – 10KHz. </p>
    <p>- Sinyal suara musik memiliki  frekuensi : 20Hz – 20Khz. </p>
    <p>- Sistem  multimedia menggunakan suara yang berada dalam range pendengaran manusia. </p>
    <p>- Suara yang berada pada range pendengaran  manusia sebagai   <strong>&ldquo;AUDIO&rdquo;</strong>, dan gelombangnya  sebagai<strong> &ldquo;ACCOUSTIC SIGNALS&rdquo; </strong>.<strong> </strong>Suara diluar range pendengaran  manusia dapat dikatakan sebagai <strong>&ldquo;NOISE&rdquo;</strong> (getaran yang tidak teratur dan  tidak berurutan dalam berbagai frekuensi, tidak dapat didengar manusia)<strong>.</strong></p>
    <p><center>
      <img width="540" height="289" src="image/suara2.jpg">
    </center></p>
    <p><strong>2. Amplitudo </strong></p>
    <p><strong>- </strong>Keras lemahnya bunyi atau tinggi  rendahnya gelombang. </p>
    <p>- Satuan amplitudo  adalah decibel (db) </p>
    <p>- Bunyi mulai dapat  merusak telinga jika tingkat volumenya lebih besar dari 85 dB dan pada ukuran  130 dB akan mampu membuat hancur gendang telinga </p>
    <p>3. <strong>Velocity </strong></p>
    <p><strong>- </strong>Kecepatan perambatan gelombang bunyi  sampai ke telinga pendengar.    </p>
    <p>- Satuan yang digunakan  : m/s </p>
    <p>- Pada udara kering  dengan suhu 20 °C (68 °F)m kecepatan rambat suara sekitar 343 m/s </p>
    <p><strong>B. REPRESENTASI SUARA</strong>
   <p>Gelombang suara  analog tidak dapat langsung direpresentasikan pada komputer. Komputer mengukur  amplitudo pada satuan waktu tertentu untuk menghasilkan sejumlah angka. Tiap  satuan pengukuran ini dinamakan <br>
      <strong>&ldquo;SAMPLE&rdquo;.</strong></p>