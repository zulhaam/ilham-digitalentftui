  <p align="justify">5.<em> Interaktivitas  Seseorang dapat mengoprasikan sendiri peralatan yang digunakan sesuai   dengan kebutuhan, yang akan memperkuat ras percaya diri pengguna (user).</em><br>
    <br>Komputer dan perangkat multimedia lain sebagai bentuk konkrit dari ICT, cepat atau lambat pasti menjadi barang kebutuhan sehari-hari, termasuk dalam pembelajaran sebagai upaya meningkatkan kualitas pendidikan. Banyak orang percaya bahwa multimedia akan membawa kita kepada situasi belajar dimana "learning with effort" (belajar dengan dipengaruhi/terpaksa) akan dapat digantikan dengan "learning with fun" (belajar dengan menyenangkan). Jika situasi tersebut tidak terjadi, maka   multimedia sekurang-kurangnya diharapkan akan dapat membantu belajar menjadi lebih efektif.
