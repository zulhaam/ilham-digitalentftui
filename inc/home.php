<h1>Apa itu Teknologi Multimedia?</h1>
  <p><center>
    <img src="image/multimedia.jpg" alt="Teknologi Multimedia" width="559" height="333">
  </center>
  </p>
  <p>Multimedia adalah gabungan dari kata "<strong>multi</strong>" dan "<strong>media</strong>". <strong>Multi</strong> berarti   banyak atau lebih dari satu, dan <strong>media</strong> berarti bentuk atau jenis sarana yang dipakai untuk menyampaikan informasi. Penggunaan televisi, video, dan film merupakan contoh penyampaian informasi yang melibatkan beberapa   komponen sekaligus, namun yang membedakan aplikasi multimedia dengan   yang lain adalah adanya interaksi antara aplikasi dengan manusia yang   menggunakannya. <br>
  </p>
  <p>Multimedia juga diartikan sebagai sistem komputer yang terdiri dari   hardware dan software yang memberikan kemudahan untuk menggabungkan   berbagai komponene seperti gambar, video, grafik, animasi, suara, teks,   dan data yang dikendalikan. dengan program komputer. Lima tipe dasar   objek media : <br>
    <br>
    <em>1. Gambar  Multimedia dapat menyediakan objek yang berupa gambar dalam bentuk foto,   grafik, peta dan gambar-gambar lain. Untuk memperoduksi gambar dapat   digunakan kamera digital, mencari gambar dari berbagai sumber, dan   download gambar yang ada di internet. </em></p>
