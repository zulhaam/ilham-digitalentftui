    <h2>    <strong>1. ZIP File Format</strong>    </h2>
    <p><center><img src="image/zip.png" width="382" height="185"></center></p>
    <p>- Ditemukan oleh <strong>Phil Katz </strong> untuk program PKZIP kemudian dikembangkan untuk WinZip, WinRAR, 7-Zip. </p>
    <p>- Berekstensi *.zip dan MIME application/zip</p>
    <p>- Dapat menggabungkan dan  mengkompresi beberapa file sekaligus menggunakan bermacam-macam algoritma,  namun paling umum menggunakan Katz&rsquo;s Deflate Algorithm. </p>
    <p>-  <span dir="LTR"> </span>    Beberapa method Zip :</p>
    <p> a. Shrinking : merupakan metode variasi  dari LZW </p>
    <p>b. Reducing  : merupakan metode yang mengkombinasikan metode same byte sequence based dan  probability based encoding. <span dir="LTR"> </span>    </p>
    <p>c. Imploding :  menggunakan metode byte sequence based dan Shannon-Fano encoding. <span dir="LTR"> </span>    Deflate : menggunakan LZW    <span dir="LTR"> </span>    Bzip2, dan lain-lain      </p>
